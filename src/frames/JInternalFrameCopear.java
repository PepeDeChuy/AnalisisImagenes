    public void buttCopear()
    {
        BufferedImage aux = herramientas.herramientas.toBufferedImage(this.JFI.getImagenSecundaria());
        int x = Integer.parseInt(jTextField_x.getText());
        int y = Integer.parseInt(jTextField_y.getText());
        int t = Integer.parseInt(jTextField_T.getText());
        Color col = Color.WHITE;
        bufferimagen = new BufferedImage(t, t, 1);

        for(int xx = 0; xx < t; xx++)
        {
            for(int yy = 0; yy < t; yy++)
            {
                bufferimagen.setRGB(xx, yy, aux.getRGB(x+xx, y+yy));
            }
        }

        Image aux2 = herramientas.herramientas.toImage(bufferimagen);
    }
    
    public void buttPegar()
    {
        BufferedImage aux = herramientas.herramientas.toBufferedImage(this.JFI.getImagenSecundaria());
        int x = Integer.parseInt(jTextField_x.getText());
        int y = Integer.parseInt(jTextField_y.getText());
        int t = Integer.parseInt(jTextField_T.getText());

        for(int xx = 0; xx < t; xx++)
        {
            for(int yy = 0; yy < t; yy++)
            {
                aux.setRGB(xx + x, yy + y, bufferimagen.getRGB(xx, yy));
            }
        }

        actualizarImagen(aux);
    }
    
    public void actualizarImagen (BufferedImage aux)
    {
        this.JFI.setImagen(herramientas.herramientas.toImage(aux));
    }
    

